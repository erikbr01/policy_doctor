"""Read all benchmark JSONs, produce Pareto plots + a markdown table.

Output:
    docs/simplification_results/_summary.md
    docs/simplification_results/_plots/{task}__{slug}__{ymetric}.png
"""

from __future__ import annotations

import json
import sys
from collections import defaultdict
from pathlib import Path

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np

_WORKTREE = Path(__file__).resolve().parents[1]
_RESULTS = _WORKTREE / "docs" / "simplification_results"
_PLOTS = _RESULTS / "_plots"
_PLOTS.mkdir(exist_ok=True, parents=True)


_PALETTE = {
    "passthrough":         "#94a3b8",
    "degree_one_prune":    "#475569",
    "hoeffding_merge":     "#ef4444",
    "chi2_merge":          "#f97316",
    "js_merge":            "#fbbf24",
    "bayesian_merge":      "#a855f7",
    "vomm_split_merge":    "#ec4899",
    "mdl_greedy":          "#10b981",
    "pcca_plus":           "#06b6d4",
    "markov_stability":    "#0ea5e9",
    "stationary_skeleton": "#84cc16",
}


_CI_FIELDS = {
    "markov_violation_bits": ("markov_ci_lo", "markov_ci_hi"),
    "markov_violation_2nd_bits": ("markov_2nd_ci_lo", "markov_2nd_ci_hi"),
    "heldout_nll_per_original_bits": ("heldout_nll_ci_lo", "heldout_nll_ci_hi"),
}


def plot_one(
    summary: dict, y_field: str, y_label: str,
    out_path: Path, title_suffix: str = "",
) -> None:
    fig, ax = plt.subplots(figsize=(7, 5), dpi=110)
    ci_lo_field, ci_hi_field = _CI_FIELDS.get(y_field, (None, None))
    for method, pts in summary["results"].items():
        pts = [p for p in pts if "error" not in p and p.get(y_field) is not None]
        if not pts:
            continue
        pts_sorted = sorted(pts, key=lambda p: (p["n_nodes"], p[y_field]))
        x = [p["n_nodes"] for p in pts_sorted]
        y = [p[y_field] for p in pts_sorted]
        color = _PALETTE.get(method, "#888")
        # Add CI band if bootstrap CIs are present
        if ci_lo_field:
            lo = [p.get(ci_lo_field) for p in pts_sorted]
            hi = [p.get(ci_hi_field) for p in pts_sorted]
            if all(v is not None for v in lo + hi):
                ax.fill_between(x, lo, hi, color=color, alpha=0.18, linewidth=0)
        ax.plot(
            x, y, marker="o", markersize=4, linewidth=1.5,
            color=color, label=method, alpha=0.85,
        )
    ax.set_xlabel("# cluster nodes (after simplification)")
    ax.set_ylabel(y_label)
    ax.set_title(
        f"{summary['task']} | k_raw={summary['n_cluster_nodes_raw']} | "
        f"N_eps={summary['n_episodes']}{title_suffix}",
        fontsize=10,
    )
    ax.grid(True, alpha=0.25)
    ax.legend(fontsize=8, ncol=2)
    fig.tight_layout()
    fig.savefig(out_path)
    plt.close(fig)


def best_at_n(pts_by_method: dict, y_field: str, n_nodes: int) -> dict:
    """For each method, the smallest y at a given n_nodes (or nearest)."""
    out = {}
    for method, pts in pts_by_method.items():
        candidates = [p for p in pts if "error" not in p and p.get(y_field) is not None]
        if not candidates:
            continue
        # Exact match first; otherwise nearest by n_nodes
        exact = [p for p in candidates if p["n_nodes"] == n_nodes]
        if exact:
            out[method] = min(exact, key=lambda p: p[y_field])
        else:
            nearest = min(candidates, key=lambda p: (abs(p["n_nodes"] - n_nodes), p[y_field]))
            out[method] = nearest
    return out


def main():
    files = sorted(_RESULTS.glob("*.json"))
    if not files:
        print(f"No results in {_RESULTS}")
        return

    summary_lines = ["# Benchmark summary\n"]
    summary_lines.append(
        "Auto-generated by `scripts/summarize_results.py`. "
        "All NLL values are in bits (log2). "
        "Markov violation = I(orig_prev; orig_next | merged_curr) in bits — "
        "lower is more Markov.\n"
    )

    for f in files:
        with f.open() as fh:
            summary = json.load(fh)
        task = summary["task"]
        slug = f.stem.split("__", 1)[1]
        summary_lines.append(f"\n## {task}  (clustering = `{slug}`)\n")
        summary_lines.append(
            f"- {summary['n_samples']} samples, {summary['n_episodes']} episodes, "
            f"raw nodes = {summary['n_cluster_nodes_raw']}, level = {summary['level']}\n"
        )

        # Pareto plots
        for y_field, y_label, fname in [
            ("markov_violation_bits",        "MV 1st-order I(prev_{t-1};next|merged) [bits] ↓",  "mv"),
            ("markov_violation_2nd_bits",    "MV 2nd-order I((prev_{t-1},prev_{t-2});next|merged) [bits] ↓",  "mv2"),
            ("heldout_nll_per_original_bits", "Held-out NLL / original transition [bits] ↓*",     "heldout"),
            ("mdl_score",                     "MDL score [bits] ↓*",                              "mdl"),
        ]:
            out_path = _PLOTS / f"{task}__{slug}__{fname}.png"
            plot_one(summary, y_field, y_label, out_path)
            rel = out_path.relative_to(_RESULTS.parent)
            summary_lines.append(f"\n![{fname}]({rel.as_posix()})\n")

        # Best methods at fixed n_nodes
        target_n = max(2, summary["n_cluster_nodes_raw"] // 3)
        rows = []
        rows.append(f"\n### Best method at n_nodes ≈ {target_n} (sorted by Markov violation):\n")
        rows.append("| method | n_nodes | lever | MV (bits) | held-NLL/orig (bits) | MDL |\n")
        rows.append("|---|---:|---:|---:|---:|---:|\n")
        best = best_at_n(summary["results"], "markov_violation_bits", target_n)
        ranked = sorted(best.items(), key=lambda kv: kv[1]["markov_violation_bits"])
        for method, p in ranked:
            ho = p.get("heldout_nll_per_original_bits")
            ho_s = f"{ho:.3f}" if ho is not None else "n/a"
            rows.append(
                f"| `{method}` | {p['n_nodes']} | {p['lever']:.4g} | "
                f"{p['markov_violation_bits']:.3f} | {ho_s} | {p['mdl_score']:.1f} |\n"
            )
        summary_lines.extend(rows)

    (_RESULTS / "_summary.md").write_text("".join(summary_lines))
    print(f"Wrote {_RESULTS / '_summary.md'}")
    print(f"Wrote plots to {_PLOTS}")


if __name__ == "__main__":
    main()
